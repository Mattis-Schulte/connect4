            color_helper = (' '.join(valid_colors.colors).replace('RED', 'Rot').replace('GREEN', 'Grün').replace('YELLOW', 'Gelb')).split()
            print(f'Bitte wählen Sie ihre Farbe ({" oder ".join([", ".join(color_helper[:-1]), color_helper[-1]])})!')
